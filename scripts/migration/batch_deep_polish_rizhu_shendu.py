def decode_hex(hex_str: str) -> str:
    """Decode hex string to UTF-8."""
    return bytes.fromhex(hex_str).decode('utf-8')


def encode_hex(text: str) -> str:
    """Encode UTF-8 text to uppercase hex string."""
    return text.encode('utf-8').hex().upper()


def replace_shendu(analysis: str, rizhu: str) -> str:
    """Replace the 【深度解读】 section in analysis with polished version."""
    if rizhu not in POLISHED_SHENDU:
        print(f"  [WARN] No polished content for {rizhu}, keeping original")
        return analysis

    polished = POLISHED_SHENDU[rizhu].strip()

    start = analysis.find('【深度解读】')
    end = analysis.find('【断语展示】')

    if start == -1:
        print(f"  [WARN] No 【深度解读】 found for {rizhu}")
        return analysis

    if end == -1:
        # No 断语展示, replace from 深度解读 to end
        new_analysis = analysis[:start] + '【深度解读】\n' + polished + '\n'
    else:
        new_analysis = analysis[:start] + '【深度解读】\n' + polished + '\n' + analysis[end:]

    return new_analysis


