CREATE TABLE IF NOT EXISTS v2_xuetang_chapters (
  id INT NOT NULL PRIMARY KEY,
  name VARCHAR(64) NOT NULL,
  description VARCHAR(255) NOT NULL DEFAULT '',
  cover_scene VARCHAR(32) NOT NULL DEFAULT '',
  sort_order INT NOT NULL DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS v2_xuetang_drops (
  sn INT NOT NULL PRIMARY KEY,
  note VARCHAR(128) NOT NULL DEFAULT '',
  item_ids JSON NOT NULL,
  rates JSON NOT NULL,
  quantities JSON NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS v2_xuetang_levels (
  sn INT NOT NULL PRIMARY KEY,
  chapter_id INT NOT NULL,
  stage_index INT NOT NULL,
  unlock_sn INT NULL COMMENT '前置关卡',
  name VARCHAR(64) NOT NULL,
  battlefield INT NOT NULL,
  reward_coin_id INT NOT NULL DEFAULT 1001,
  reward_coin_num INT NOT NULL DEFAULT 0,
  reward_xp_id INT NOT NULL DEFAULT 1002,
  reward_xp_num INT NOT NULL DEFAULT 0,
  drop_id INT NOT NULL,
  start_drama_id INT NULL,
  end_drama_id INT NULL,
  lesson_body TEXT NULL,
  quiz_json JSON NULL,
  UNIQUE KEY uk_chapter_stage (chapter_id, stage_index),
  KEY idx_chapter (chapter_id),
  KEY idx_unlock (unlock_sn)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS v2_xuetang_user_progress (
  user_id BIGINT NOT NULL,
  level_sn INT NOT NULL,
  score INT NULL,
  completed_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (user_id, level_sn),
  KEY idx_user_completed (user_id),
  CONSTRAINT fk_xuetang_prog_user FOREIGN KEY (user_id) REFERENCES v2_yonghu_profiles(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

