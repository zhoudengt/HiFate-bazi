-- 与 v2_liuyao_castings 无外键依赖

CREATE TABLE IF NOT EXISTS v2_yonghu_profiles (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  guest_token VARCHAR(64) NOT NULL,
  nickname VARCHAR(50) DEFAULT NULL,
  avatar_url VARCHAR(512) DEFAULT NULL,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uk_guest_token (guest_token),
  KEY idx_guest_token (guest_token)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS v2_youxi_level_config (
  level INT NOT NULL PRIMARY KEY,
  xp_required BIGINT NOT NULL,
  description VARCHAR(100) DEFAULT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS v2_youxi_states (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  user_id BIGINT NOT NULL,
  level INT NOT NULL DEFAULT 1,
  xp BIGINT NOT NULL DEFAULT 0,
  destiny_points BIGINT NOT NULL DEFAULT 0,
  tree_level INT NOT NULL DEFAULT 1,
  tree_last_water_date DATE DEFAULT NULL,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uk_user_id (user_id),
  CONSTRAINT fk_v2_game_user FOREIGN KEY (user_id) REFERENCES v2_yonghu_profiles(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS v2_youxi_xp_logs (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  user_id BIGINT NOT NULL,
  amount INT NOT NULL,
  source VARCHAR(50) NOT NULL,
  source_detail VARCHAR(200) DEFAULT NULL,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  KEY idx_user_source (user_id, source),
  CONSTRAINT fk_v2_xp_user FOREIGN KEY (user_id) REFERENCES v2_yonghu_profiles(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS v2_youxi_points_logs (
  id BIGINT AUTO_INCREMENT PRIMARY KEY,
  user_id BIGINT NOT NULL,
  amount INT NOT NULL,
  source VARCHAR(50) NOT NULL,
  source_detail VARCHAR(200) DEFAULT NULL,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  KEY idx_user_source (user_id, source),
  CONSTRAINT fk_v2_points_user FOREIGN KEY (user_id) REFERENCES v2_yonghu_profiles(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

